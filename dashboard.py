def generate_pdf_report(df, actual, predicted, model_name, score):

    file_name = "house_price_prediction_report.pdf"

    styles = getSampleStyleSheet()
    elements = []

    # -------------------------
    # Title
    # -------------------------

    elements.append(Paragraph("House Price Prediction Report", styles['Title']))
    elements.append(Spacer(1,20))

    # -------------------------
    # Project Description
    # -------------------------

    description = """
    This report summarizes the predictions generated by the machine learning
    model trained on the housing dataset. The system predicts house prices
    using features such as bedrooms, bathrooms, square footage, and year built.
    """

    elements.append(Paragraph(description, styles['Normal']))
    elements.append(Spacer(1,20))

    # -------------------------
    # Dataset Summary
    # -------------------------

    dataset_data = [
        ["Dataset Detail", "Value"],
        ["Total Records", len(df)],
        ["Total Features", len(df.columns)-1],
        ["Target Variable", "price"]
    ]

    table = Table(dataset_data)

    table.setStyle([
        ('BACKGROUND',(0,0),(-1,0),colors.grey),
        ('TEXTCOLOR',(0,0),(-1,0),colors.whitesmoke),
        ('GRID',(0,0),(-1,-1),1,colors.black)
    ])

    elements.append(Paragraph("Dataset Summary", styles['Heading2']))
    elements.append(table)
    elements.append(Spacer(1,20))

    # -------------------------
    # Features Used
    # -------------------------

    features = [
        "bedrooms","bathrooms","sqft_living","sqft_lot","floors",
        "waterfront","view","condition","sqft_above",
        "sqft_basement","yr_built","yr_renovated"
    ]

    elements.append(Paragraph("Features Used for Prediction", styles['Heading2']))

    for f in features:
        elements.append(Paragraph(f"- {f}", styles['Normal']))

    elements.append(Spacer(1,20))

    # -------------------------
    # Scatter Plots Section
    # -------------------------

    elements.append(Paragraph("Feature vs Price Scatter Plots", styles['Heading2']))
    elements.append(Spacer(1, 10))

    for feature in features:

        plt.figure()

        plt.scatter(df[feature], df["price"])

        plt.xlabel(feature)
        plt.ylabel("PRICE")
        plt.title(f"{feature} vs PRICE")

        image_name = f"{feature}_scatter.png"

        plt.savefig(image_name)
        plt.close()

        elements.append(Paragraph(f"{feature} vs PRICE", styles['Normal']))
        elements.append(Image(image_name, width=400, height=250))

        # Explanation for scatter plot
        explanation = f"""
        This scatter plot shows the relationship between the feature <b>{feature}</b> 
        and the target variable <b>PRICE</b>. Each point represents an individual 
        house from the dataset.

        Scatter plots help visualize whether a relationship exists between the 
        feature and house prices. Observing the distribution of points allows us 
        to understand how variations in <b>{feature}</b> may influence the predicted 
        price of houses.
        """

        elements.append(Paragraph(explanation, styles['Normal']))
        elements.append(Spacer(1,20))

    # -------------------------
    # Model Information
    # -------------------------

    elements.append(Paragraph("Model Information", styles['Heading2']))

    elements.append(Paragraph(f"Best Model Selected: {model_name}", styles['Normal']))
    elements.append(Paragraph(f"R-Squared Score: {round(score,4)}", styles['Normal']))

    elements.append(Spacer(1,20))

    # -------------------------
    # Actual vs Predicted Table
    # -------------------------

    elements.append(Paragraph("Actual vs Predicted Prices", styles['Heading2']))

    table_data = [["Actual Price","Predicted Price"]]

    for a,p in zip(actual,predicted):
        table_data.append([round(a,2), round(p,2)])

    table = Table(table_data)

    table.setStyle([
        ('BACKGROUND',(0,0),(-1,0),colors.grey),
        ('TEXTCOLOR',(0,0),(-1,0),colors.whitesmoke),
        ('GRID',(0,0),(-1,-1),1,colors.black)
    ])

    elements.append(table)
    elements.append(Spacer(1,20))

    # -------------------------
    # Prediction Graph
    # -------------------------

    x = np.arange(len(actual))

    plt.figure()

    plt.bar(x-0.2, actual, width=0.4)
    plt.bar(x+0.2, predicted, width=0.4)

    plt.xlabel("Samples")
    plt.ylabel("Price")
    plt.title("Actual vs Predicted Prices")

    plt.savefig("prediction_graph.png")

    elements.append(Paragraph("Prediction Visualization", styles['Heading2']))
    elements.append(Image("prediction_graph.png", width=400, height=250))

    # Explanation for prediction graph
    prediction_explanation = """
    This bar chart compares the actual house prices from the dataset with the 
    prices predicted by the machine learning model.

    The visualization helps evaluate how closely the model's predictions match 
    the real values. If the predicted bars are close to the actual bars, it 
    indicates that the model is performing well in estimating house prices.
    """

    elements.append(Paragraph(prediction_explanation, styles['Normal']))

    elements.append(Spacer(1,20))

    # -------------------------
    # Timestamp
    # -------------------------

    elements.append(Paragraph(
        f"Report Generated On: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        styles['Normal']
    ))

    # Build PDF

    pdf = SimpleDocTemplate(file_name, pagesize=letter)
    pdf.build(elements)

    return file_name

